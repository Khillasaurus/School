//----------------------------------------------------------------------------------------------------
//Constuctors
//----------------------------------------------------------------------------------------------------

cFrequencyElement::cFrequencyElement()
:	mLetter(0)
,	mPriority(0)
{
}

//----------------------------------------------------------------------------------------------------

cFrequencyElement::cFrequencyElement(char letter, int priority)
:	mLetter(letter)
,	mPriority(priority)
{
}

//----------------------------------------------------------------------------------------------------
//Deconstructor
//----------------------------------------------------------------------------------------------------

cFrequencyElement::~cFrequencyElement()
{
}

//----------------------------------------------------------------------------------------------------
//Overloaded operators
//----------------------------------------------------------------------------------------------------

bool cFrequencyElement::operator < (const cFrequencyElement& frequencyElement) const
{
	return mPriority > frequencyElement.GetPriority();
}

//----------------------------------------------------------------------------------------------------

bool cFrequencyElement::operator > (const cFrequencyElement& frequencyElement) const
{
	return mPriority < frequencyElement.GetPriority();
}

//----------------------------------------------------------------------------------------------------
//Getters
//----------------------------------------------------------------------------------------------------

char cFrequencyElement::GetLetter() const
{
	return mLetter;
}

//----------------------------------------------------------------------------------------------------

int cFrequencyElement::GetPriority() const
{
	return mPriority;
}

//----------------------------------------------------------------------------------------------------
//Setters
//----------------------------------------------------------------------------------------------------

void cFrequencyElement::SetLetter(char letter)
{
	mLetter = letter;
}

//----------------------------------------------------------------------------------------------------

void cFrequencyElement::SetPriority(int priority)
{
	mPriority = priority;
}
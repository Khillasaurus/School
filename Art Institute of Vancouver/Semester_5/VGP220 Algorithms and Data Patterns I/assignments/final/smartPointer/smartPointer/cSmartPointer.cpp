//----------------------------------------------------------------------------------------------------
//Constuctors
//----------------------------------------------------------------------------------------------------

cSmartPointer::cSmartPointer(char* sentence)
{
	pActualData = new cActualData(sentence);
}

//----------------------------------------------------------------------------------------------------
//Deconstructor
//----------------------------------------------------------------------------------------------------

cSmartPointer::~cSmartPointer()
{
	if(pActualData)
	{
		delete pActualData;
		pActualData = NULL;
	}
}

//----------------------------------------------------------------------------------------------------
//Member Functions
//----------------------------------------------------------------------------------------------------

void cSmartPointer::PrintData() const
{
	printf("%s\n", pActualData->mSentence);
}
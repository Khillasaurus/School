//-------------------------------------------------------------------------------------------------
// Constructors
//-------------------------------------------------------------------------------------------------

Fighter::Fighter()
	: mPosition(0.0f, 0.0f)
	, mSpeed(0.0f)
{
	//Empty
}

//-------------------------------------------------------------------------------------------------
// Deconstructor
//-------------------------------------------------------------------------------------------------

Fighter::~Fighter()
{
	//Empty
}

//-------------------------------------------------------------------------------------------------
//SGE Specific
//-------------------------------------------------------------------------------------------------

void Fighter::Load()
{
	mSprite.Load("fighter.png");
}

//-------------------------------------------------------------------------------------------------

void Fighter::Unload()
{
	mSprite.Unload();
}

//-------------------------------------------------------------------------------------------------

void Fighter::Update(float deltaTime)
{
	//Movement
	if(Input_IsKeyDown(Keys::RIGHT))
	{
		mPosition.x += mSpeed * deltaTime;
	}
	else if(Input_IsKeyDown(Keys::LEFT))
	{
		mPosition.x -= mSpeed * deltaTime;
	}
	if(Input_IsKeyDown(Keys::UP))
	{
		mPosition.y -= mSpeed * deltaTime;
	}
	else if(Input_IsKeyDown(Keys::DOWN))
	{
		mPosition.y += mSpeed * deltaTime;
	}
}

//-------------------------------------------------------------------------------------------------

void Fighter::Render()
{
	const float kHalfWidth = mSprite.GetWidth() * 0.5f;
	const float kHalfHeight = mSprite.GetHeight() * 0.5f;
	SVector2 renderPos(mPosition.x - kHalfWidth, mPosition.y - kHalfHeight);
	mSprite.SetPosition(renderPos);
	mSprite.Render();
}
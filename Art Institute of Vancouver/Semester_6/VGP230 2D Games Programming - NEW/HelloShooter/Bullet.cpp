//-------------------------------------------------------------------------------------------------
// Constructors
//-------------------------------------------------------------------------------------------------

Bullet::Bullet()
	: mPosition(0.0f, 0.0f)
	, mVelocity(0.0f, 0.0f)
	, mActive(false)
{
	//Empty
}

//-------------------------------------------------------------------------------------------------
// Deconstructor
//-------------------------------------------------------------------------------------------------

Bullet::~Bullet()
{
	//Empty
}

//-------------------------------------------------------------------------------------------------
//SGE Specific
//-------------------------------------------------------------------------------------------------

void Bullet::Load()
{
	mSprite.Load("bullet1.png");
}

//-------------------------------------------------------------------------------------------------

void Bullet::Unload()
{
	mSprite.Unload();
}

//-------------------------------------------------------------------------------------------------

void Bullet::Update(float deltaTime)
{
	if(IsActive())
	{
		mPosition += mVelocity * deltaTime;

		// HACK: Assume the bullet only goes upwards
		if(mPosition.y < -100.0f)
		{
			Kill();
		}
	}
}

//-------------------------------------------------------------------------------------------------

void Bullet::Render()
{
	if(IsActive())
	{
		const float kHalfWidth = mSprite.GetWidth() * 0.5f;
		const float kHalfHeight = mSprite.GetHeight() * 0.5f;
		SVector2 renderPos(mPosition.x - kHalfWidth, mPosition.y - kHalfHeight);
		mSprite.SetPosition(renderPos);
		mSprite.Render();
	}
}

//-------------------------------------------------------------------------------------------------

void Bullet::Fire(const SVector2& pos, const SVector2& vel)
{
	mPosition = pos;
	mVelocity = vel;
	mActive = true;
}

//-------------------------------------------------------------------------------------------------

void Bullet::Kill()
{
	mActive = false;
}


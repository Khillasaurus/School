int cNode::autoRefCount = 0;

//====================================================================================================
//Class Definitions
//====================================================================================================

//----------------------------------------------------------------------------------------------------
//Constuctors
//----------------------------------------------------------------------------------------------------

cNode::cNode()
:	pVehicle(NULL)
,	pNext(NULL)
,	pPrev(NULL)
{
	++cNode::autoRefCount;
}

//----------------------------------------------------------------------------------------------------
//Deconstructor
//----------------------------------------------------------------------------------------------------

cNode::~cNode()
{
	if(pVehicle)
	{
		delete pVehicle;
		pVehicle = NULL;
	}
	--cNode::autoRefCount;
}
void Random::SetSeed(u32 seed)
{
	srand(seed);
}

//----------------------------------------------------------------------------------------------------

f32 Random::GetF()
{
	return (f32)(rand()) / (f32)RAND_MAX;
}

//----------------------------------------------------------------------------------------------------

f32 Random::GetF(f32 min, f32 max)
{
	return min + (GetF() * (max - min));
}